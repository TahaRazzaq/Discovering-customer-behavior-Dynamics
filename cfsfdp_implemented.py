  len_c = len(column)
  iter = int( len_c/48 )

  for i in range(iter):

    x = i*48
    y = (i+1)*48 + 1
    max = column[x:y].max()
    min = column[x:y].min()
    column[x:y] = (column[x:y] - min) / (max - min)

    clear_output(wait=True)
    print(i,"/",iter)
  return column

