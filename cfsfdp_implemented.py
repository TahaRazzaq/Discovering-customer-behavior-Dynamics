def kld_calculation(pi,pj):
    total = 0
    for m in range(N):
        for n in range(N):
            value = pi[m][n] * np.log(pi[m][n]/pj[m][n])
            if np.isfinite(value):
                total = total + value
    kld = (1/N) * total
    
    return kld

def distance_cal(pi,pj):
    distance = (kld_calculation(pi,pj) + kld_calculation(pj,pi))/2
    return distance

